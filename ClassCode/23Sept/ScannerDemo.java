import java.util.*;

class ScannerDemo {

	public static void main (String [] args)   {

		Scanner sc  = new Scanner(System.in);

		int x = sc.nextInt();

		String y = sc.next();
		
		float f = sc.nextFloat();

		char h = sc.next().charAt(0);

		System.out.println(x);
		System.out.println(y);
		System.out.println(f);
		System.out.println(h);



	}




}
